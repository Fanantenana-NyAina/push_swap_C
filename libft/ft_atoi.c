#include "libft.h"

int	ft_atoi(const char *p)
{
	int	i;
	int	j;
	int	sign;

	i = 0;
	while ((p[i] >= 9 && p[i] <= 13) || p[i] == 32)
		i++;
	sign = 1;
	if (p[i] == '+' || p[i] == '-')
	{
		if (p[i] == '-')
			sign *= -1;
		i++;
	}
	j = 0;
	while (p[i] >= '0' && p[i] <= '9')
	{
		j = j * 10 + (p[i] - '0');
		i++;
	}
	return (j * sign);
}
